def make_case(
    case_id: str,
    question: str,
    expected_answer: str,
    difficulty: str,
    category: str,
    source_docs: List[str],
    notes: str,
) -> Dict:
    return {
        "id": case_id,
        "question": question,
        "ground_truth": expected_answer,
        "expected_answer": expected_answer,
        "difficulty": difficulty,
        "category": category,
        "expected_retrieval_ids": source_docs,
        "source_docs": source_docs,
        "notes": notes,
        "expert_reviewed": True,
    }


